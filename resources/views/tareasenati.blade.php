     <h1>cuy chactado</h1>
     <h3>Hasta este punto te preguntaras! ¿Los cusqueños aman el cuy?
      La verdad es que si, es un plato muy popular no solo en Cusco y Machu Picchu, si no en todo el Perú, porque tiene un sabor    exquisito y es altamente nutritivo.
      Para prepararlo, la carne de cuy se fríe en aceite y se suele servir con papas hervidas y maíz. Además, se dice que este tipo de carne ya se consumía hace más de 5000 años en el Perú Antiguo.</h3>
